package it.eng.mapping;

public class Server {
	private int port;
	private String baseRoot;
	
	public Server() {}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}

	public String getBaseRoot() {
		return baseRoot;
	}

	public void setBaseRoot(String baseRoot) {
		this.baseRoot = baseRoot;
	}

	
	
	
}
