package it.eng.mapping;

public class ContextBroker {
	private String host;
	private int port;
	
	public ContextBroker() {}

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}
	
	
}
