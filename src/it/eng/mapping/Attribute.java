package it.eng.mapping;

public class Attribute {
	public String name;
	public String type;
	
	public Attribute() {}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
	
}
